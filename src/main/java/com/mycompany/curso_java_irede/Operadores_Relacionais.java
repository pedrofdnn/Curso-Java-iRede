package com.mycompany.curso_java_irede;

public class Operadores_Relacionais {

    public static void main(String[] args) {

        int idade = 18;

        System.out.println("Maior de idade => " + (idade >= 18));
        System.out.println("Menor de idade => " + (idade < 18));
        System.out.println("Idade e igual a 18? => " + (idade == 18));
        System.out.println("Idade e diferente a 21? => " + (idade != 18));

    }
}
