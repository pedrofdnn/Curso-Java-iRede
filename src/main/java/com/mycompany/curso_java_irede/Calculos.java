package com.mycompany.curso_java_irede;

public class Calculos {

    public static void main(String[] args) {
//      soma
        int a = 1;
        int b = 20;
        System.out.println(a + b);

//      subtração
        int c = 5;
        int d = 25;
        System.out.println(d - c);
//      OBS: para o valor ficar positivo e necessário 
//      deixr o maior valor em primeiro

//      multiplicação
        int e = 5;
        int f = 2;
        System.out.println(e * f);

//        Divisão
        int g = 2;
        int h = 20;
        System.out.println(h / g);
//        OBS: fazer o mesmo procedimento da subtração

//        Resto
        int i = 10;
        int j = 3;
        System.out.println(i % j);
    }

}
